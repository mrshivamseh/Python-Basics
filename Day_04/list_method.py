fruits = ["apple", "banana", "cherry"]

# add item
fruits.append("orange")
print(fruits)

# changing item
fruits[1] = "kiwi"
print(fruits)

# Remove item
fruits.remove("cherry")
print(fruits)

# add item at specific position
fruits.insert(1, "mango")
print(fruits)

